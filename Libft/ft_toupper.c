int	ft_toupper(int c)
{
	unsigned char	cc;

	if (c < 0)
		cc = -c;
	else
		cc = (unsigned char)c;
	if (cc >= 'a' && cc <= 'z')
		cc = cc - 32;
	return (cc);
}
