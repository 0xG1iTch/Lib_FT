#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int	g;

	g = 0;
	while (s1[g] && s2[g] && (s1[g] == s2[g]) && n)
	{
		g++;
		n--;
	}
	if (n == 0)
		return (0);
	else
		return (s1[g] - s2[g]);
}
